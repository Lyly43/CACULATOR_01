    //ket qua thuc hien cua cac phep tinh + - * /
    private void btnketQuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnketQuaActionPerformed
        if(jTextField1.getText().equals("")||operand1.equals("")){
            return;
        }
        
        try {
            double giaTri1 = Double.parseDouble(operand1);
            double giaTri2 = Double.parseDouble(jTextField1.getText());
            double ketQua = 0;
            
            if(operator.equals("+")){
                
                ketQua = giaTri1 + giaTri2;
            }
            else if(operator.equals("-")){
                ketQua = giaTri1 - giaTri2;
            }
            else if(operator.equals("*")){
                ketQua = giaTri1 * giaTri2;
            }
            else if(operator.equals("/")){
                ketQua = giaTri1 / giaTri2;
            }
            
            jTextField1.setText("" + ketQua);
            
            checkEquals = true;
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnketQuaActionPerformed
    
