    //thuc hien sqrt va %
    private void btnCanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCanActionPerformed
        if(jTextField1.getText().equals("")){
            return;
        }
        String cmd = evt.getActionCommand();
        
        try {
            double giaTri = Double.parseDouble(jTextField1.getText());
            
            if(cmd.equals("sqrt")){
                giaTri = Math.sqrt(giaTri);
            }
            else if(cmd.equals("%")){
                giaTri = giaTri * 100;
            }
        
            jTextField1.setText("" + giaTri);
        
            checkEquals = true;
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCanActionPerformed

